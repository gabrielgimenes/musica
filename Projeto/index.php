<h2 style="text-align: left;">O que é Música</h2>
<p>A pergunta “o que é música” tem sido alvo de discussão há décadas.Alguns autores defendem que música é a combinação de sons e silêncios de uma maneira organizada. Vamos explicar com um exemplo: Um ruído de rádio emite sons, mas não de uma forma organizada, por isso não é classificado como música. Essa definição parece simples e completa, mas definir música não é algo tão óbvio assim. Podemos classificar um alarme de carro como música? Ele emite sons e silêncios de uma maneira organizada, mas garanto que a maioria das pessoas não chamaria esse som de música.
</p>

<h2>Então, o que é música afinal</h2>
<p>De uma maneira mais didática e abrangente, a música é composta por <b> melodia</b>, <b>harmonia</b> e <b>ritmo</b>.</p>

<table id="tabelaspec">
<!--Tabela-->
<tr><td class="cd"><b>Melodia</b></td><td class="cd"> É a voz principal do som, é aquilo que pode ser cantado </td></tr>
<tr><td class="cd"><b>Harmonia<b></td><td class="cd"> É uma sobreposição de notas que servem de base para a melodia. Por exemplo, uma pessoa tocando violão e cantando está fazendo harmonia com os acordes no violão e melodia com a voz. </td></tr>
<tr><td class="cd"><b>Ritmo</b></td><td class="cd"> É a marcação do tempo de uma música. Assim como o relógio marca as horas, o ritmo nos diz como acompanhar a música  </td></tr>
</table>
